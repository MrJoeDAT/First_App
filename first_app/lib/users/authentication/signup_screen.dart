  validateUserEmail() async {
    try {
      var res = await http.post(
        Uri.parse(API.validateEmail),
        body: {
          'user_email': emailController.text.trim(),
        },
      );

      print("Response status code: ${res.statusCode}");
      print("Response body: ${res.body}");

      if (res.statusCode == 200) {
        var resBodyOfValidateEmail = jsonDecode(res.body);

        print("Decoded response body: $resBodyOfValidateEmail");

        if (resBodyOfValidateEmail['emailFound'] == true) {
          Fluttertoast.showToast(
              msg: "Email already in use, Try another email.");
        } else {
          // Register and save new user record to database
          registerAndSaveUserRecord();
        }
      } else {
        print("Failed to validate email. Status code: ${res.statusCode}");
        Fluttertoast.showToast(msg: "Failed to validate email.");
      }
    } catch (e) {
      print("Error validating email: $e");
      Fluttertoast.showToast(msg: "Error validating email.");
    }
  }

  registerAndSaveUserRecord() async {
    User userModel = User(
      1,
      nameController.text.trim(),
      emailController.text.trim(),
      passwordController.text.trim(),
    );

    try {
      var res = await http.post(
        Uri.parse(API.signUp),
        body: userModel.toJson(),
      );

      if (res.statusCode ==
          200) // From flutter app the connection with api to server - Success
      {
        var resBodyOfSignUp = jsonDecode(res.body);

        print("Response body: $resBodyOfSignUp");

        if (resBodyOfSignUp['success'] == true) {
          Fluttertoast.showToast(msg: "Congratulations, SignUp Successful.");

          setState(() {
            nameController.clear();
            emailController.clear();
            passwordController.clear();
          });
        } else {
          Fluttertoast.showToast(msg: "Error Occurred, Try Again.");
        }
      } else {
        print("Failed to sign up. Status code: ${res.statusCode}");
      }
    } catch (e) {
      print("Error signing up: $e");
      Fluttertoast.showToast(msg: "Error signing up.");
    }
  }

